    public ArrayList<HallDetailsDbHandling> ListUsers(String valToSearch)
    {
        ArrayList<HallDetailsDbHandling> userlist=new ArrayList<HallDetailsDbHandling>();
        Statement st;
        ResultSet rst;
        try
        {
            st=con.createStatement();
            String searchQuery="SELECT * FROM `hall` WHERE CONCAT(`hall_No`, `hall_Name`, `hall_Place`) AND hall_Status=0 LIKE'%"+valToSearch+"%'";
            rst=st.executeQuery(searchQuery);
            
            HallDetailsDbHandling dh;
            while(rst.next())
            {
                dh=new HallDetailsDbHandling(
                                    rst.getString("hall_No"),
                                    rst.getString("hall_Name"),
                                    rst.getString("hall_Place")
                                    
                        );
                userlist.add(dh);
            }
            
        }
        catch(Exception ex)
        {
            System.out.println(ex.getMessage());
        }
        return userlist;
    }
    
    //search list
    public void findUsers()
    {
        ArrayList<HallDetailsDbHandling> users=ListUsers(txthallSearch.getText());
        DefaultTableModel model= new DefaultTableModel();
        model.setColumnIdentifiers(new Object[]{"HallID","HallName","Place"});
        Object[] row=new Object[3];
        for(int i=0 ; i<users.size(); i++)
        {
            row[0]=users.get(i).getHallId();
            row[1]=users.get(i).getHallName();
            row[2]=users.get(i).getHallPlace();
            model.addRow(row);
        }
        halldetailsTable.setModel(model);
    }
    
