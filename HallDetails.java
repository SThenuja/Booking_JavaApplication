    //auto increment hall id
    public void generateHallId()
    {
    try
    {
        String query="Select count('hall_No')as countNo from hall";
        PreparedStatement pst=con.prepareStatement(query);
        ResultSet rs=pst.executeQuery();
        //int i=rs.getInt(query);

        while(rs.next())
        {
        int num = rs.getInt("countNo");
        int inc=num+1;
        lb_hallno.setText(valueOf(inc));
        lb_hallno.setVisible(true);

        }

    }

    catch(Exception a)
    {
        a.printStackTrace();
    }
    }
//load table
    public void tableload()
   {
       try 
       {
         String sql = "SELECT hall_No,hall_Name,hall_Place FROM hall WHERE hall_Status=1";
        PreparedStatement pst = con.prepareStatement(sql);
        ResultSet rs = pst.executeQuery();
         
         halldetailsTable.setModel(net.proteanit.sql.DbUtils.resultSetToTableModel(rs));
         
       } 
       catch (Exception e) 
       {
           System.out.println(e);
       }
   }
